import mongoose from "mongoose";

const SampleSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      default: Date.now,
    },
  },
  { timestamps: true }
);

const Sample = mongoose.models.Sample || mongoose.model("Sample", SampleSchema);

export default Sample;
